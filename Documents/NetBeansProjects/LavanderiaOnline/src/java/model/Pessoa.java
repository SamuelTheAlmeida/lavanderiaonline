/**
 *
 * @author SAMUEL
 */
@Entity
@Table(name="Nomes")
public class Pessoa {
    private int id;
    private String nome;

    @Id
    @GeneratedValue(strategy=GenerationType.AUTO)
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
    
}
